    public void atualizarLista(){
        modelo = (DefaultTableModel)jTable1.getModel();
        modelo.setNumRows(0);
        linhas.clear();
         for (Paciente c : Principal.controladorPaciente.getPacientes()){
             linhas.add(new Object[]{c.getId(), c.getNome(),c.getRg(),c.getData().toString(), c.getTelefone(),
                    c.getCelular(), c.getEndereco().getEstado(), c.getEndereco().getCidade(),c.getEndereco().getBairro(),
                    c.getEndereco().getRua(), c.getEndereco().getNumero(), c.getEndereco().getComplemento()});
         }
         for (Object t[]: linhas){
            if (textField1.getText().equals("")){
                modelo.addRow(t);
             }
            else{
                String _n = (String) t[1];
                String procurarPor = textField1.getText();
                if (_n.toLowerCase().contains(procurarPor.toLowerCase()))
                    modelo.addRow(t);
            }
                
         }
         atualizarImagem();
    }
