    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jButton3.getText().equals("Selecionar")){
            if (jTable1.getSelectedRow()!=-1){
                int _id = (int) modelo.getValueAt(jTable1.getSelectedRow(),0);
                telaAdicionar.idP = _id;
                telaAdicionar.jLabel7.setText(Principal.controladorPaciente.getPacienteID(_id).getNome());
                dispose();
            }
        }
        else
        {
            if (jTable1.getSelectedRow()!=-1){
                if (JOptionPane.showConfirmDialog(null, "Você tem certeza?", "Atenção!",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    int a[] = jTable1.getSelectedRows();
                    for (int b : a){
                        Principal.controladorPaciente.deletar((int) modelo.getValueAt(b, 0));
                        File imagem = new File(localPath+"/Perfis/"+(int) modelo.getValueAt(b, 0)+".jpg");
                        if (imagem.exists())
                            imagem.delete();
                    }
                    atualizarLista();
                    JOptionPane.showMessageDialog(null, "Registro(s) excluido(s)");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

